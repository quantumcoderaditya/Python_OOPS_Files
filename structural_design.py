# Structural Design Patterns - structural Design Patterns focus on the composition of classes and objects to form larger structures and systems and these patterns primarly deals with how objects and classes can be combined to form larger more complex structures while keeping these structures flexible and efficient. So the key objectives of structural design patterns is to provide solutions to design problems related to object composition and structure, allowing for better organization and management of code. 

# Advatages of strcutural Design Patterns :

# 1.  Promote code reusability and modularity
# 2.  Enhance flexibilty and extensibility
# 3.  Improve performance and resource utilization