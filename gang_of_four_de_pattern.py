# # The Gang of Four Design Patterns - It consists of 23 design patterns. These 23 design patterns can be grouped into three categories 

# 1. Creational - The different ways to create objects
# 2. Structural - The relationship between those objects
# 3. Behavioral - The interaction or communication between those objects.

# - Behavioural : 
#   o State Pattern
#   o Observer Pattern

# - Structural :
#   o Facade Pattern
#   o Adapter Pattern 

# - Creational
#   o Prototype Pattern
#   o Abstract Factory Pattern 
